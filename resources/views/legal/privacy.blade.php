<h1>AIP - Atención Integral al Paciente</h1>

<h2>Política de Privacidad</h2>

<p>
Esta aplicación recopila únicamente la información necesaria para brindar servicios médicos y gestionar turnos, recetas, estudios, certificados y consultas.
</p>

<p>
Los datos personales se almacenan de forma segura y no son compartidos con terceros, salvo cuando sea necesario para prestar el servicio o por requerimiento legal.
</p>

<p>
Los pagos son procesados mediante Mercado Pago. La aplicación no almacena datos de tarjetas de crédito.
</p>

<p>
Las notificaciones push se utilizan únicamente para informar sobre novedades relacionadas con la atención médica.
</p>

<p>
El usuario puede solicitar la eliminación de sus datos comunicándose con el administrador de la aplicación.
</p>

